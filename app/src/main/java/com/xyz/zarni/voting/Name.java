package com.xyz.zarni.voting;

public class Name {

    String king,queen;

    public Name() {
    }

    public Name(String king, String queen) {
        this.king = king;
        this.queen = queen;
    }

    public String getKing() {
        return king;
    }

    public void setKing(String king) {
        this.king = king;
    }

    public String getQueen() {
        return queen;
    }

    public void setQueen(String queen) {
        this.queen = queen;
    }
}
